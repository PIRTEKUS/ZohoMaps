def _get_admin_access_token():
    """Get a fresh admin access token using the best available refresh token.
    Uses database-backed caching to prevent concurrent requests from rotating
    and invalidating the refresh token, and to avoid Zoho API rate limits.
    """
    env_refresh = os.environ.get('ZOHO_REFRESH_TOKEN', '').strip()
    db_encrypted = database.get_global_setting('admin_refresh_token', '')
    db_refresh = decrypt_token(db_encrypted) if db_encrypted else ''

    tokens_to_try = []
    if env_refresh:
        tokens_to_try.append((env_refresh, 'env-var'))
    if db_refresh:
        tokens_to_try.append((db_refresh, 'db-token'))

    # First loop: Try to load from cache without making any Zoho network calls
    for refresh_token, source in tokens_to_try:
        ref_hash = hashlib.sha256(refresh_token.encode()).hexdigest()
        try:
            cached_atk_enc = database.get_global_setting('cached_admin_access_token', '')
            cached_expires_at = database.get_global_setting('cached_admin_access_token_expires_at', '0')
            cached_ref_hash = database.get_global_setting('cached_admin_access_token_ref_hash', '')

            if cached_atk_enc and cached_expires_at and cached_ref_hash == ref_hash:
                if time.time() < float(cached_expires_at) - 60:
                    decrypted = decrypt_token(cached_atk_enc)
                    if decrypted:
                        return decrypted
        except Exception as e:
            log_debug(f"[admin_token] Error checking access token cache for {source}: {e}")

    # Second loop: No valid cache matches, so refresh token from Zoho
    for refresh_token, source in tokens_to_try:
        try:
            token_data = zoho_api.refresh_access_token(refresh_token)
            if 'access_token' in token_data:
                new_access_token = token_data['access_token']
                expires_in = token_data.get('expires_in', 3600)
                new_rt = token_data.get('refresh_token', '').strip()

                # Handle token rotation: save the new refresh token to DB if from DB,
                # and update the refresh token reference to compute correct hash
                used_rt = refresh_token
                if new_rt:
                    if source == 'db-token':
                        database.set_global_setting('admin_refresh_token', encrypt_token(new_rt))
                        log_debug(f"[admin_token] Refresh token rotated ({source}) — new token saved to DB.")
                        used_rt = new_rt
                    else:
                        log_debug(f"[admin_token] Refresh token rotated ({source}) but cannot write to env var.")

                # Compute new hash of the active refresh token
                new_ref_hash = hashlib.sha256(used_rt.encode()).hexdigest()

                # Cache the new access token
                database.set_global_setting('cached_admin_access_token', encrypt_token(new_access_token))
                database.set_global_setting('cached_admin_access_token_expires_at', str(time.time() + expires_in))
                database.set_global_setting('cached_admin_access_token_ref_hash', new_ref_hash)

                return new_access_token
            else:
                log_debug(f"[admin_token] Token refresh failed for {source}: {token_data.get('error', 'unknown error')}")
        except Exception as e:
            log_debug(f"[admin_token] Token refresh exception for {source}: {e}")

    return None

def _require_admin_token(context='sync'):
    """Return admin access token for server-side API calls.
    All data syncs use the admin token so user profile API restrictions
    do not limit which modules/records can be fetched.
    Falls back to user session token if no admin token is stored yet."""
    token = _get_admin_access_token()
    if token:
        return token
    log_debug(f"[{context}] WARNING: No admin token — using user session token (degraded mode).")
    from flask import session as _s
    return _s.get('access_token')


def _cache_module_url_map(access_token):
    """Fetch module metadata and store the api_name→module_name map in the DB.
    Called by admin login and nightly sync so non-admin users can always
    resolve the correct system tab name (e.g. CustomModule2) for custom modules."""
    try:
        module_metadata = zoho_api.fetch_module_metadata(access_token)
        if 'modules' not in module_metadata:
            log_debug(f"[_cache_module_url_map] Fetch failed: {module_metadata.get('message','?')}")
            return
        url_map = {}
        label_map = {}
        for m in module_metadata['modules']:
            api_name = m.get('api_name', '')
            if not api_name:
                continue
            label_map[api_name] = m.get('plural_label', api_name)
            if m.get('generated_type') == 'custom':
                # module_name is always the immutable system name (e.g. 'CustomModule2')
                url_map[api_name] = m.get('module_name', api_name)
            else:
                url_map[api_name] = api_name
        database.set_global_setting('cached_module_url_map', json.dumps(url_map))
        # Also refresh the simpler cached_modules list used by the settings page
        modules_list = [{'api_name': k, 'plural_label': v} for k, v in label_map.items()]
        database.set_global_setting('cached_modules', json.dumps(modules_list))
        log_debug(f"[_cache_module_url_map] Cached {len(url_map)} module URL mappings.")
    except Exception as e:
        log_debug(f"[_cache_module_url_map] Exception: {e}")


# ── Location-aware API filtering helpers ─────────────────────────────────────

def _build_location_filter_criteria(fields):
    """Build a Zoho search criteria string that skips records where ALL location
    fields (lat/lng and address fields) are null.  This eliminates unmappable
    records at the API level, saving API pages and geocoding quota.

    Returns a criteria string like:
        ((City:is_not_null) or (State:is_not_null) or (Latitude__c:is_not_null))
    or None if no location fields are configured for this module.
    """
    location_keys = ['latitude', 'longitude', 'address1', 'city', 'state', 'zip']
    seen = set()
    zoho_fields = []
    for k in location_keys:
        f = fields.get(k)
        if f and f not in seen:
            seen.add(f)
            zoho_fields.append(f)
    if not zoho_fields:
        return None
    parts = [f"({f}:is_not_null)" for f in zoho_fields]
    return parts[0] if len(parts) == 1 else "(" + " or ".join(parts) + ")"


def _combine_criteria(criteria_a, criteria_b):
    """AND two Zoho search criteria strings together.
    Either can be None (returns the other one)."""
    if criteria_a and criteria_b:
        return f"({criteria_a} and {criteria_b})"
    return criteria_a or criteria_b


def _is_null_string(val):
    """Return True if val is the literal string 'NULL'/'None' that Zoho
    sometimes returns for unconfigured field names."""
    return val is not None and str(val).strip().upper() in ('NULL', 'NONE', 'N/A', 'UNKNOWN')


def _refresh_user_mappings(admin_token):
    """Fetch all territories and their users, building a mapping of email -> user details."""
    try:
        t_url = f"{zoho_api.ZOHO_API_URL}/crm/v3/settings/territories"
        headers = {'Authorization': f'Zoho-oauthtoken {admin_token}'}
        r = requests.get(t_url, headers=headers, timeout=10)
        if not r.ok:
            log_debug(f"[user_mappings] Failed to fetch territories: {r.status_code} - {r.text}")
            return None
        
        territories = r.json().get('territories', [])
        email_lookup = {}
        for t in territories:
            t_name = t.get('name')
            t_id = t.get('id')
            u_url = f"{zoho_api.ZOHO_API_URL}/crm/v3/settings/territories/{t_id}/users"
            u_resp = requests.get(u_url, headers=headers, timeout=10)
            if u_resp.ok:
                t_users = u_resp.json().get('users', [])
                for u in t_users:
                    email = (u.get('email') or '').strip().lower()
                    if email:
                        if email not in email_lookup:
                            email_lookup[email] = {
                                'id': str(u.get('id')),
                                'name': u.get('full_name', ''),
                                'franchise': u.get('Franchise', ''),
                                'territories': []
                            }
                        if t_name not in email_lookup[email]['territories']:
                            email_lookup[email]['territories'].append(t_name)
        
        # Save to database
        database.set_global_setting('user_territory_mappings', json.dumps(email_lookup))
        log_debug(f"[user_mappings] Rebuilt user mapping cache. Found {len(email_lookup)} users in territories.")
        return email_lookup
    except Exception as e:
        log_debug(f"[user_mappings] Error building cache: {e}")
        return None


def _get_all_franchises(admin_token):
    """Fetch all Franchises from CRM custom module to resolve names to IDs."""
    try:
        headers = {'Authorization': f'Zoho-oauthtoken {admin_token}'}
        resp = requests.get(
            f'{zoho_api.ZOHO_API_URL}/crm/v3/Franchises',
            headers=headers,
            params={'fields': 'id,Name,Pirtek_Franchise_ID,Franchise_Standard_Users,Franchise_Admin_User', 'per_page': 200},
            timeout=10
        )
        if resp.ok:
            data = resp.json().get('data', [])
            if data:
                try:
                    simple_list = [{'id': str(f['id']), 'name': f.get('Name')} for f in data if f.get('Name') and f.get('id')]
                    database.set_global_setting('cached_all_franchises', json.dumps(simple_list))
                except Exception as cache_err:
                    log_debug(f"[franchises] Cache write failed: {cache_err}")
            return data
        log_debug(f"[franchises] Failed to fetch custom module records: {resp.status_code}")
    except Exception as e:
        log_debug(f"[franchises] Error fetching franchises: {e}")
    return []


def _get_user_franchise_ids(user_id, admin_token, force_refresh=False):
    """Return franchise memberships for a user.
    Uses cached mapping derived from Zoho territories and user profiles,
    and falls back to standard user profile queries.
    """
    import time
    cache_key = f'franchise_ids_{user_id}'
    _cached_raw = database.get_global_setting(cache_key, '')
    _cached_data = None
    if _cached_raw:
        try:
            _cached_data = json.loads(_cached_raw)
        except Exception:
            pass

    # Return fresh cache (< 4h) without hitting the API
    if not force_refresh and _cached_data:
        if time.time() - _cached_data.get('ts', 0) < 14400:
            return _cached_data

    if not admin_token:
        # fallback to stale cache if available
        if _cached_data:
            return _cached_data
        return None

    debug_log = []
    found = {}

    # Get user email and numeric ID
    user_email = str(user_id).lower() if '@' in str(user_id) else None
    resolved_id = str(user_id) if not user_email else None

    # Load or rebuild the territory-to-user mappings cache
    mappings_raw = database.get_global_setting('user_territory_mappings', '')
    mappings = {}
    if mappings_raw:
        try:
            mappings = json.loads(mappings_raw)
        except Exception:
            pass

    # If the user_id or email is not in the cache, rebuild it on-demand
    user_key = user_email if user_email else None
    if not user_key and resolved_id:
        for em, udet in mappings.items():
            if udet.get('id') == resolved_id:
                user_key = em
                break

    if force_refresh or not user_key or user_key not in mappings:
        debug_log.append("Rebuilding territory-to-user mappings cache on-demand...")
        new_mappings = _refresh_user_mappings(admin_token)
        if new_mappings:
            mappings = new_mappings
            if not user_key and resolved_id:
                for em, udet in mappings.items():
                    if udet.get('id') == resolved_id:
                        user_key = em
                        break
            elif user_email:
                user_key = user_email

    # Now look up user details in the mappings cache
    user_detail = mappings.get(user_key) if user_key else None
    user_franchise_name = None
    user_territories = []
    if user_detail:
        resolved_id = user_detail.get('id')
        user_franchise_name = user_detail.get('franchise')
        user_territories = user_detail.get('territories', [])
        debug_log.append(f"Found user mapping: Email={user_key}, CRM_ID={resolved_id}, Franchise_Field='{user_franchise_name}', Territories={user_territories}")
        
        if user_franchise_name:
            # Query all custom franchises to find a match by Name
            franchises = _get_all_franchises(admin_token)
            for f in franchises:
                f_name = f.get('Name')
                f_id = str(f.get('id'))
                f_pirtek = f.get('Pirtek_Franchise_ID')
                if f_name and f_name.strip().lower() == user_franchise_name.strip().lower():
                    found[f_id] = {
                        'id': f_id,
                        'name': f_name,
                        'pirtek_id': f_pirtek or ''
                    }
                    debug_log.append(f"Mapped user Franchise field '{user_franchise_name}' to record ID {f_id}")
                    break
    else:
        debug_log.append(f"User '{user_id}' not found in territory user mapping cache.")

    # ── Strategy 1: COQL lookup
    # Check if the user is listed in any Franchise record's "Franchise_Standard_Users" or "Franchise_Admin_User" field.
    if resolved_id:
        for coql in [
            f"SELECT id, Name, Pirtek_Franchise_ID FROM Franchises WHERE Franchise_Standard_Users = '{resolved_id}'",
            f"SELECT id, Name, Pirtek_Franchise_ID FROM Franchises WHERE Franchise_Admin_User = '{resolved_id}'"
        ]:
            try:
                result = zoho_api.coql_query(coql, admin_token)
                recs = result.get('data', [])
                for rec in recs:
                    fid = str(rec.get('id', ''))
                    if fid and fid not in found:
                        found[fid] = {
                            'id': fid,
                            'name': rec.get('Name', ''),
                            'pirtek_id': rec.get('Pirtek_Franchise_ID', '') or ''
                        }
            except Exception as e:
                debug_log.append(f'COQL error: {e}')

    # ── Strategy 2: Territory-based matching
    # Match territory name to franchise name
    if user_detail and user_territories:
        franchises = _get_all_franchises(admin_token)
        for tname in user_territories:
            matched = False
            for f in franchises:
                f_name = f.get('Name', '').strip().lower()
                f_id = str(f.get('id'))
                f_pirtek = f.get('Pirtek_Franchise_ID')
                t_lower = tname.strip().lower()
                
                # Check if Franchise Name is similar
                tokens = [tok for tok in t_lower.replace('_', ' ').split() if len(tok) > 2]
                if any(tok in f_name for tok in tokens) or f_name in t_lower or t_lower in f_name:
                    found[f_id] = {
                        'id': f_id,
                        'name': f.get('Name'),
                        'pirtek_id': f_pirtek or ''
                    }
                    debug_log.append(f"Mapped territory '{tname}' to similar Franchise '{f.get('Name')}'")
                    matched = True
                    break
            if not matched:
                found[f'territory_{tname}'] = {
                    'id': f'territory_{tname}',
                    'name': tname,
                    'pirtek_id': ''
                }

    # Save to cache
    results = {
        'ids':       [v['id']        for v in found.values()],
        'names':     [v['name']      for v in found.values()],
        'pirtek_ids':[v['pirtek_id'] for v in found.values()],
        'debug':     debug_log,
        'ts':        time.time()
    }

    database.set_global_setting(cache_key, json.dumps(results))
    if user_email and resolved_id:
        database.set_global_setting(f'franchise_ids_{resolved_id}', json.dumps(results))
        
    log_debug(f"[franchise_ids] user {user_id} → {len(results['ids'])} franchise(s): {results['names']}")
    return results

def build_fields_list(module_name, config, field_metadata):
    """Build a list of up to 50 field API names to fetch from Zoho, prioritizing configured ones."""
    fields = config.get('field_mappings', {})
    priority_fields = set()
    
    # Add mapped address and location fields
    for key in ['address1', 'address2', 'city', 'state', 'zip', 'country', 'latitude', 'longitude']:
        v = fields.get(key)
        if v:
            priority_fields.add(v)
            
    # Add title field
    title_field = fields.get('title_field')
    if title_field:
        priority_fields.add(title_field)
    else:
        priority_fields.add('Account_Name' if module_name == 'Accounts' else ('Full_Name' if module_name in ['Leads', 'Contacts'] else 'Name'))
        
    # Add duplicate filter fields
    df = fields.get('duplicate_filter', {})
    if df.get('enabled'):
        for key in ['parent_link_field', 'primary_code_field', 'override_checkbox_field']:
            v = df.get(key)
            if v:
                priority_fields.add(v)
                
    # Add standard/system fields we always need
    priority_fields.update(['id', 'Modified_Time'])
    
    # Add franchise lookup field
    franchise_field = _NIGHTLY_FRANCHISE_FIELD_MAP.get(module_name)
    if franchise_field:
        priority_fields.add(franchise_field)

    # Add configured additional fields
    additional = fields.get('additional_fields', [])
    priority_fields.update([f for f in additional if f])

    if not field_metadata or 'fields' not in field_metadata:
        return list(priority_fields)
        
    # Get all available API names from metadata
    all_api_fields = [f['api_name'] for f in field_metadata['fields']]
    
    # We want to fill the list up to 50. Filter out unneeded system subforms
    excluded_keywords = ['_s', 'Locked__s', 'Tag', 'Currency', 'Exchange_Rate']
    
    available_fields = []
    for f in all_api_fields:
        if f in priority_fields:
            continue
        if any(kw in f for kw in excluded_keywords):
            continue
        available_fields.append(f)
        
    final_fields = list(priority_fields)
    for f in available_fields:
        if len(final_fields) >= 50:
            break
        if f not in final_fields:
            final_fields.append(f)
            
    return final_fields


def do_sync_single_record(user_id, access_token, module_name, record_id, config):
    log_debug(f"Starting single sync for module: {module_name}, record: {record_id} for user {user_id}...")
    
    fields = config['field_mappings']
    title_field = fields.get('title_field')
    name_field = title_field if title_field else ('Account_Name' if module_name == 'Accounts' else ('Full_Name' if module_name in ['Leads', 'Contacts'] else 'Name'))
            
    field_metadata = zoho_api.fetch_module_fields(module_name, access_token)
    field_label_map = {}
    if 'fields' in field_metadata:
        for f in field_metadata['fields']:
            field_label_map[f['api_name']] = f['display_label']
    else:
        cached = database.get_global_setting(f'cached_fields_{module_name}', '')
        if cached:
            try:
                for f in json.loads(cached):
                    field_label_map[f['api_name']] = f['display_label']
            except: pass

    sync_fields_list = build_fields_list(module_name, config, field_metadata)

    data = zoho_api.fetch_single_record(module_name, record_id, access_token, fields=sync_fields_list)
    
    if 'code' in data and data.get('status') == 'error':
        error_code = data.get('code')
        error_msg  = data.get('message', '')
        log_debug(f"Zoho API error for {module_name}/{record_id}: [{error_code}] {error_msg}")
        if error_code in ('NO_PERMISSION', 'EMPTY_RESPONSE', 'INVALID_JSON'):
            log_debug(f"Attempting admin token fallback for {record_id}...")
            admin_token = _get_admin_access_token()
            if admin_token and user_id:
                criteria = f"((Owner.id:equals:{user_id})and(id:equals:{record_id}))"
                owner_data = zoho_api.search_records(module_name, criteria, admin_token, fields=sync_fields_list)
                if 'data' in owner_data and owner_data['data']:
                    data = owner_data
                else:
                    log_debug(f"Admin fallback returned 0 records for {module_name}/{record_id}. Recording as hidden.")
                    # Save tombstone to database to hide this record for the user
                    database.save_module_records_batch(user_id, [(record_id, module_name, "Hidden Record", None, None, "", {'_hidden': True})])
                    return 'hidden'
            else:
                log_debug("No admin token available for fallback. Recording as hidden.")
                database.save_module_records_batch(user_id, [(record_id, module_name, "Hidden Record", None, None, "", {'_hidden': True})])
                return 'hidden'
        else:
            return False

    records_list = data.get('data', [])
    if not records_list:
        log_debug(f"Single sync failed: No records returned for {module_name} {record_id}. Raw response: {data}. Recording as hidden.")
        database.save_module_records_batch(user_id, [(record_id, module_name, "Hidden Record", None, None, "", {'_hidden': True})])
        return 'hidden'
        
    record = records_list[0]
    
    lat, lng = None, None
    name_raw = record.get(name_field, record.get('Full_Name', record.get('Name', f"{module_name} {record.get('id')}")))
    name = str(extract_val(name_raw))
    
    lat_field = fields.get('latitude')
    lng_field = fields.get('longitude')
    if lat_field and lng_field and record.get(lat_field) and record.get(lng_field):
        try:
            lat = float(record[lat_field])
            lng = float(record[lng_field])
        except (ValueError, TypeError):
            pass
    
    if lat is None or lng is None:
        address_parts = []
        for k in ['address1', 'address2', 'city', 'state', 'zip', 'country']:
            val = extract_val(record.get(fields.get(k)))
            if val and not _is_null_string(val):
                address_parts.append(str(val))
        
        full_address = ", ".join(address_parts)
        if full_address:
            lat, lng = geocode_address(full_address)
    
    if lat is not None and lng is not None:
        # Build record_data as the entire record payload (raw JSON)
        record_data = record.copy()
        if record.get('Modified_Time') and '_modified_time' not in record_data:
            record_data['_modified_time'] = str(record.get('Modified_Time'))
            
        # Store parent link ID for client-side deduplication (raw dict id, not display name)
        df = fields.get('duplicate_filter', {})
        if df.get('enabled') and df.get('parent_link_field'):
            raw_parent = record.get(df['parent_link_field'])
            if isinstance(raw_parent, dict) and 'id' in raw_parent:
                record_data['_dup_parent_id'] = raw_parent['id']
            elif raw_parent:
                record_data['_dup_parent_id'] = str(raw_parent)

        database.save_module_records_batch(user_id, [(
            record.get('id'),
            module_name,
            name,
            lat,
            lng,
            config['marker_color'],
            record_data
        )])
        
        log_debug(f"--- Raw Zoho API Data for {name} ({record_id}) ---")
        for k in fetch_fields_list:
            log_debug(f"RAW {k}: {record.get(k)}")

        log_debug(f"--- Extracted Record Data for {name} ({record_id}) ---")
        for k, v in record_data.items():
            log_debug(f"{k}: {v}")
        log_debug(f"--------------------------------------------------")
        
        log_debug(f"Successfully synced and saved single record: {name} ({record_id})")
        return 'synced'
    else:
        log_debug(f"Single sync failed: No valid location found for {name} ({record_id}). Record data: {record}")
        return False

