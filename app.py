# ── Token encryption key ─────────────────────────────────────────────────────
# Stored in environment variable TOKEN_ENCRYPTION_KEY (generated by setup script).
# Fall back to a key derived from the app secret for backward-compatibility during
# first-run, but emit a warning so the admin knows to set the env var.
_raw_fernet_key = os.environ.get('TOKEN_ENCRYPTION_KEY', '')
if _raw_fernet_key:
    _cipher = Fernet(_raw_fernet_key.encode())
else:
    import base64, hashlib
    _derived = base64.urlsafe_b64encode(hashlib.sha256(_secret_key.encode()).digest())
    _cipher = Fernet(_derived)
    print("WARNING: TOKEN_ENCRYPTION_KEY env var not set. Using derived key. "
          "Set TOKEN_ENCRYPTION_KEY in /etc/systemd/system/zohomap.service for production.")

def encrypt_token(token: str) -> str:
    return _cipher.encrypt(token.encode()).decode()

def decrypt_token(token: str) -> str:
    try:
        return _cipher.decrypt(token.encode()).decode()
    except Exception:
        return ''

