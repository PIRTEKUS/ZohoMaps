@app.before_request
def check_token_refresh():
    if 'access_token' in session and 'expires_at' in session:

        # 1. Refresh token if near expiry
        if time.time() > session['expires_at'] - 300:
            if 'refresh_token' in session:
                token_data = zoho_api.refresh_access_token(session['refresh_token'])
                if 'access_token' in token_data:
                    session['access_token'] = token_data['access_token']
                    session['expires_at'] = time.time() + token_data.get('expires_in', 3600)

        # 2. Fetch user info if missing
        if 'user_id' not in session or 'is_admin' not in session:
            try:
                user_info = zoho_api.fetch_user_info(session['access_token'])
                if 'users' in user_info and len(user_info['users']) > 0:
                    user = user_info['users'][0]
                    session['user_id'] = user['id']
                    session['user_name'] = user.get('full_name', user.get('last_name', 'Zoho User'))
                    profile = user.get('profile', {})
                    profile_name = profile.get('name', '')
                    session['is_admin'] = (profile_name.lower() in ['administrator', 'admin'])
                    log_debug(f"LOGIN INFO: Name={session['user_name']}, Profile={profile_name}, IsAdmin={session['is_admin']}, ID={session['user_id']}")
            except Exception as e:
                log_debug(f"DEBUG: Failed to fetch user info: {str(e)}")

        # 3. Detect org/domain — runs INDEPENDENTLY for every user when missing from session
        if 'org_id' not in session or 'domain_name' not in session:
            org_fetched = False
            try:
                org_info = zoho_api.fetch_org_metadata(session['access_token'])
                log_debug(f"DEBUG Org API raw response keys: {list(org_info.keys())} | code={org_info.get('code','?')}")
                if 'org' in org_info and len(org_info['org']) > 0:
                    org = org_info['org'][0]
                    log_debug(f"DEBUG: Full Org Data: {json.dumps(org)}")
                    session['org_id'] = org.get('zgid') or org.get('zoid') or org.get('id')
                    session['domain_name'] = org.get('domain_name', '')
                    org_fetched = True
                    log_debug(f"AUTO-DETECTED: OrgID={session['org_id']}, Domain={session['domain_name']}")
                    if session['org_id'] and not database.get_global_setting('crmplus_orgid', ''):
                        database.set_global_setting('crmplus_orgid', str(session['org_id']))
                        log_debug(f"Saved OrgID to global settings: {session['org_id']}")
                    if session['domain_name'] and not database.get_global_setting('crmplus_domain', ''):
                        database.set_global_setting('crmplus_domain', session['domain_name'])
                        log_debug(f"Saved Domain to global settings: {session['domain_name']}")
                else:
                    # v6 failed — try v3 as fallback
                    log_debug(f"Org v6 failed (code={org_info.get('code','?')}), trying v3...")
                    org_info_v3 = zoho_api.fetch_org_metadata_v3(session['access_token'])
                    log_debug(f"DEBUG Org v3 raw: {json.dumps(org_info_v3)[:300]}")
                    if 'org' in org_info_v3 and len(org_info_v3['org']) > 0:
                        org = org_info_v3['org'][0]
                        session['org_id'] = org.get('zgid') or org.get('zoid') or org.get('id')
                        session['domain_name'] = org.get('domain_name', '')
                        org_fetched = True
                        log_debug(f"AUTO-DETECTED (v3): OrgID={session['org_id']}, Domain={session['domain_name']}")
                        if session['org_id'] and not database.get_global_setting('crmplus_orgid', ''):
                            database.set_global_setting('crmplus_orgid', str(session['org_id']))
                        if session['domain_name'] and not database.get_global_setting('crmplus_domain', ''):
                            database.set_global_setting('crmplus_domain', session['domain_name'])
            except Exception as org_err:
                log_debug(f"DEBUG: Org API exception: {str(org_err)}")

            # Fallback: load from global DB (written by admin session)
            if not org_fetched:
                stored_org_id = database.get_global_setting('crmplus_orgid', '')
                stored_domain  = database.get_global_setting('crmplus_domain', '')
                if stored_org_id:
                    session['org_id'] = stored_org_id
                    log_debug(f"Loaded OrgID from global settings: {stored_org_id}")
                if stored_domain:
                    session['domain_name'] = stored_domain
                    log_debug(f"Loaded Domain from global settings: {stored_domain}")
                if not stored_org_id and not stored_domain:
                    log_debug("WARNING: org/domain not found via API or global settings. Admin must log in or set manually in Settings.")


def _get_target_record_coordinates(module_name, record_id, effective_configs, user_id):
    """Try to find the target record coordinates in database, or sync on-the-fly if not found."""
    if not module_name or not record_id:
        return None, None, None
        
    matched_config = next((c for c in effective_configs if c['module_name'].lower() == module_name.lower()), None)
    if not matched_config:
        matched_config = next((c for c in effective_configs if c.get('module_label', '').lower() == module_name.lower()), None)
        
    resolved_module_name = matched_config['module_name'] if matched_config else module_name
    
    conn = database.get_db_connection()
    try:
        row = database.exec_query(
            conn,
            "SELECT lat, lng FROM module_records WHERE id = ? LIMIT 1",
            (record_id,),
            fetchone=True
        )
    except Exception as e:
        log_debug(f"Database error querying record ID {record_id}: {e}")
        row = None
    finally:
        conn.close()
    
    target_lat = None
    target_lng = None
    if row:
        target_lat = row['lat'] if isinstance(row, dict) else row[0]
        target_lng = row['lng'] if isinstance(row, dict) else row[1]
    else:
        if matched_config:
            sync_token = _require_admin_token(f'sync-deep-link/{resolved_module_name}')
            if sync_token:
                try:
                    result = do_sync_single_record(user_id, sync_token, resolved_module_name, record_id, matched_config)
                    if result == 'synced' or result is True:
                        conn = database.get_db_connection()
                        try:
                            row2 = database.exec_query(
                                conn,
                                "SELECT lat, lng FROM module_records WHERE id = ? LIMIT 1",
                                (record_id,),
                                fetchone=True
                            )
                        except Exception:
                            row2 = None
                        finally:
                            conn.close()
                        if row2:
                            target_lat = row2['lat'] if isinstance(row2, dict) else row2[0]
                            target_lng = row2['lng'] if isinstance(row2, dict) else row2[1]
                except Exception as e:
                    log_debug(f"Failed to sync deep link record: {e}")
                    
    return resolved_module_name, target_lat, target_lng


def _get_franchises_for_ui(user_id, is_admin):
    """Get the list of franchises to display in the UI filter dropdown."""
    franchises = []
    
    if is_admin:
        # Load from cache first
        raw = database.get_global_setting('cached_all_franchises', '')
        if raw:
            try:
                franchises = json.loads(raw)
            except Exception:
                pass
        
        # If still empty, try to fetch using admin token on-the-fly
        if not franchises:
            token = _get_admin_access_token()
            if token:
                data = _get_all_franchises(token)
                if data:
                    franchises = [{'id': str(f['id']), 'name': f.get('Name')} for f in data if f.get('Name') and f.get('id')]
                    # cache it
                    database.set_global_setting('cached_all_franchises', json.dumps(franchises))
    else:
        # Standard user: load from their cached franchise info
        _cache_key = f"franchise_ids_{user_id}"
        _cached_raw = database.get_global_setting(_cache_key, '')
        _fi = None
        if _cached_raw:
            try:
                _fi = json.loads(_cached_raw)
            except Exception:
                pass
        
        # If cache not found or expired, try to fetch using admin token
        if not _fi:
            token = _get_admin_access_token()
            if token:
                _fi = _get_user_franchise_ids(user_id, token)
        
        if _fi:
            for fid, fname in zip(_fi.get('ids', []), _fi.get('names', [])):
                if not str(fid).startswith('territory_'):
                    franchises.append({'id': str(fid), 'name': fname})
                    
    # Sort franchises by name alphabetically for UI convenience
    franchises = sorted(franchises, key=lambda x: x['name'].lower() if x.get('name') else '')
    return franchises


def _get_cluster_config():
    """Load the marker cluster configuration, with defaults."""
    cluster_config_str = database.get_global_setting('cluster_config', '{}')
    try:
        cluster_config = json.loads(cluster_config_str)
    except Exception:
        cluster_config = {}
    
    defaults = {
        'b1_from': 2,
        'b1_to': 10,
        'b1_color': '#3b82f6', # Blue
        'b2_to': 50,
        'b2_color': '#f59e0b', # Orange
        'b3_color': '#ef4444'  # Red
    }
    for k, v in defaults.items():
        cluster_config.setdefault(k, v)
    
    try:
        cluster_config['b1_from'] = int(cluster_config.get('b1_from', 2))
        cluster_config['b1_to'] = int(cluster_config['b1_to'])
        cluster_config['b2_to'] = int(cluster_config['b2_to'])
    except (ValueError, TypeError):
        cluster_config['b1_from'] = 2
        cluster_config['b1_to'] = 10
        cluster_config['b2_to'] = 50
        
    return cluster_config


