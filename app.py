# Franchise field per module — must match FRANCHISE_FIELD_MAP in do_sync_module
_NIGHTLY_FRANCHISE_FIELD_MAP = {
    'Accounts':          'Franchise',
    'Leads':             'Select_Your_Franchise1',
    'Ship_To_Addresses': 'Franchise',
}

_nightly_sync_running = False   # simple guard (per-worker); timer uses its own process


def _nightly_sync_module(admin_token, module_name, config):
    """Fetch ALL records for one module and store in the global cache (__global__ user).
    Returns the number of records saved."""
    log_debug(f"[nightly] Starting module: {module_name}")
    fields = config['field_mappings']

    # Build field list (same logic as do_sync_module)
    fetch_fields = set()
    for k, v in fields.items():
        if k == 'additional_fields' and isinstance(v, list):
            fetch_fields.update([f for f in v if f])
        elif k == 'duplicate_filter':
            pass
        elif isinstance(v, str) and v:
            fetch_fields.add(v)

    df = fields.get('duplicate_filter', {})
    if df.get('enabled'):
        for key in ['parent_link_field', 'primary_code_field', 'override_checkbox_field']:
            v = df.get(key)
            if v:
                fetch_fields.add(v)

    title_field = fields.get('title_field')
    if title_field:
        name_field = title_field
    else:
        name_field = 'Name'
        if module_name == 'Accounts':
            name_field = 'Account_Name'
        elif module_name == 'Leads' or module_name == 'Contacts':
            name_field = 'Full_Name'

    fetch_fields.add(name_field)
    fetch_fields.add('id')
    fetch_fields.add('Modified_Time')

    # Include franchise lookup field so we can store it for per-user filtering
    franchise_field = _NIGHTLY_FRANCHISE_FIELD_MAP.get(module_name)
    if franchise_field:
        fetch_fields.add(franchise_field)

    fetch_fields_list = list(fetch_fields)

    # Field label map for record_data display labels
    field_metadata = zoho_api.fetch_module_fields(module_name, admin_token)
    field_label_map = {}
    if 'fields' in field_metadata:
        for f in field_metadata['fields']:
            field_label_map[f['api_name']] = f['display_label']
        try:
            fields_to_cache = [{'api_name': f['api_name'], 'display_label': f['display_label']} for f in field_metadata['fields']]
            database.set_global_setting(f'cached_fields_{module_name}', json.dumps(fields_to_cache))
        except Exception as e:
            log_debug(f"Failed to cache fields in nightly sync for {module_name}: {e}")

    sync_fields_list = build_fields_list(module_name, config, field_metadata)

    # Load existing global cache to optimize geocoding/sync costs
    cached_records = database.get_global_records_by_module(module_name)
    cache_by_id = {str(r['id']): r for r in cached_records}
    active_ids = set()

    count = 0
    page = 1
    page_token = None
    more_records = True

    # Nightly sync: always fetch ALL records with no API-level filter.
    # Zoho's v3 search API does not support 'is_not_null' — it silently returns
    # 0 records instead of an error.  Records with no mappable location are
    # discarded locally when geocoding fails (see address_parts logic below).
    log_debug(f"[nightly] {module_name}: fetching all records (full global cache).")

    db_conn = database.get_db_connection()
    while more_records:
        log_debug(f"[nightly] {module_name}: fetching page {page}...")
        data = zoho_api.fetch_module_records(module_name, admin_token, fields=sync_fields_list,
                                             page=page, page_token=page_token)

        if 'code' in data and data.get('status') == 'error':
            log_debug(f"[nightly] API error {module_name}: {data.get('code')} – {data.get('message')}")
            break

        if 'data' not in data or not data['data']:
            log_debug(f"[nightly] {module_name}: no data returned for page {page}.")
            break

        records_fetched = len(data['data'])
        log_debug(f"[nightly] {module_name}: fetched {records_fetched} records for page {page}.")

        page_records = []
        should_fetch_details = (module_name != 'Ship_To_Addresses')
        cache_hits = 0
        cache_misses = 0
        for record in data['data']:
            record_id = record.get('id')
            if not record_id:
                continue

            zoho_modified = record.get('Modified_Time')
            cached_item = cache_by_id.get(str(record_id))

            use_cache = False
            if cached_item:
                cached_modified = cached_item['record_data'].get('_modified_time')
                if cached_modified and zoho_modified and cached_modified == zoho_modified:
                    if cached_item['lat'] is not None and cached_item['lng'] is not None:
                        use_cache = True

            if use_cache:
                page_records.append((
                    record_id, module_name, cached_item['name'], cached_item['lat'], cached_item['lng'],
                    config['marker_color'], cached_item['record_data'], cached_item['franchise_id']
                ))
                active_ids.add(str(record_id))
                count += 1
                cache_hits += 1
                continue

            cache_misses += 1
            # Cache miss: geocode (record already has all fields fetched by fetch_module_records)

            lat, lng = None, None
            name_raw = record.get(name_field, record.get('Name',
                                  record.get('Full_Name', f"{module_name} {record.get('id')}")))
            name = str(extract_val(name_raw))

            # Coordinates first
            lat_field = fields.get('latitude')
            lng_field = fields.get('longitude')
            if lat_field and lng_field and record.get(lat_field) and record.get(lng_field):
                try:
                    lat = float(record[lat_field])
                    lng = float(record[lng_field])
                except (ValueError, TypeError):
                    pass

            # Fallback to address geocoding
            if lat is None or lng is None:
                address_parts = []
                for k in ['address1', 'address2', 'city', 'state', 'zip', 'country']:
                    val = extract_val(record.get(fields.get(k)))
                    if val and not _is_null_string(val):
                        address_parts.append(str(val))
                full_address = ', '.join(address_parts)
                if full_address:
                    lat, lng = geocode_address(full_address, conn=db_conn)

            if lat is not None and lng is not None:
                # Extract franchise ID — handles regular lookup (dict) AND
                # multiuserlookup fields (list of dicts, e.g. Select_Your_Franchise1)
                franchise_id = None
                if franchise_field:
                    fval = record.get(franchise_field)
                    if isinstance(fval, list):
                        # Multiuserlookup: list of {id, name} dicts — take the first
                        first = next((item for item in fval if isinstance(item, dict)), None)
                        franchise_id = str(first.get('id', '')) or None if first else None
                    elif isinstance(fval, dict):
                        franchise_id = str(fval.get('id', '')) or None
                    elif fval:
                        franchise_id = str(fval)

                # Store raw payload
                record_data = record.copy()
                if record.get('Modified_Time') and '_modified_time' not in record_data:
                    record_data['_modified_time'] = str(record.get('Modified_Time'))

                # Store parent link ID for client-side deduplication
                df = fields.get('duplicate_filter', {})
                if df.get('enabled') and df.get('parent_link_field'):
                    raw_parent = record.get(df['parent_link_field'])
                    if isinstance(raw_parent, dict) and 'id' in raw_parent:
                        record_data['_dup_parent_id'] = raw_parent['id']
                    elif raw_parent:
                        record_data['_dup_parent_id'] = str(raw_parent)

                page_records.append((
                    record.get('id'), module_name, name, lat, lng,
                    config['marker_color'], record_data, franchise_id
                ))
                active_ids.add(str(record.get('id')))
                count += 1

        log_debug(f"[nightly] {module_name} page {page} summary: {cache_hits} cache hits, {cache_misses} cache misses. Saving batch of {len(page_records)} records...")
        if page_records:
            database.save_global_records_batch(page_records)

        info = data.get('info', {})
        more_records = info.get('more_records', False)
        page_token = info.get('next_page_token')
        if more_records:
            page += 1
        if page > 500:
            break

    # Soft deletion cleanup of stale records (not returned by Zoho)
    database.delete_stale_global_records(module_name, active_ids)

    # Close persistent database connection
    db_conn.close()

    log_debug(f"[nightly] {module_name}: {count} records saved to global cache.")
    return count


def do_nightly_sync():
    """Orchestrates the nightly full sync of all shared modules into the global cache.
    Called by the systemd timer (via run_nightly_sync.py) and the admin manual trigger."""
    log_debug("[nightly] ========== Nightly sync started ==========")
    admin_token = _get_admin_access_token()
    if not admin_token:
        log_debug("[nightly] FAILED: No admin token. Admin must log in to ZohoMap first.")
        database.set_global_setting('last_nightly_sync_results',
                                    json.dumps({'error': 'No admin token available'}))
        return {'error': 'No admin token available'}

    configs = database.get_shared_configs()
    if not configs:
        log_debug("[nightly] No shared module configs found — nothing to sync.")
        return {'error': 'No shared configs'}

    results = {}
    total = 0
    start = datetime.datetime.now(datetime.timezone.utc)

    for config in configs:
        module_name = config['module_name']
        try:
            count = _nightly_sync_module(admin_token, module_name, config)
            results[module_name] = count
            total += count
        except Exception as e:
            log_debug(f"[nightly] {module_name}: ERROR – {e}")
            results[module_name] = f'error: {e}'

    # Rebuild user territory mappings cache nightly
    try:
        _refresh_user_mappings(admin_token)
    except Exception as e:
        log_debug(f"[nightly] Error rebuilding user mappings: {e}")

    # Refresh module URL map cache nightly (keeps non-admin links working)
    try:
        _cache_module_url_map(admin_token)
    except Exception as e:
        log_debug(f"[nightly] Error refreshing module URL map: {e}")

    elapsed = round((datetime.datetime.now(datetime.timezone.utc) - start).total_seconds())
    database.set_global_setting('last_nightly_sync',
                                datetime.datetime.now(datetime.timezone.utc).isoformat())
    database.set_global_setting('last_nightly_sync_results', json.dumps(results))
    log_debug(f"[nightly] Complete in {elapsed}s. Total: {total} records. {results}")
    return {'total': total, 'elapsed_seconds': elapsed, 'results': results}


